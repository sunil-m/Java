package RemoteControl;

public class GarbageDoor {
	
	public GarbageDoor() {}
	
    public void up() {
    	System.out.println("Garbage door is up");
    }
    
    public void down() {
    	System.out.println("Garbage door is down");
    }
    
    public void stop() {
    	System.out.println("Garbage door is stop");
    }
    
    public void lighton() {
    	System.out.println("Garbage door light is on");
    }
    
    public void lightoff() {
    	System.out.println("Garbage door light is off");
    }
}
